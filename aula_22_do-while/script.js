/*
    DO WHILE
        - Repete até que a condição especificada seja VERDADEIRA.
        No entanto, a instrução será executada pelo menos uma vez
        antes da condição ser verificada.
*/

let value = 0;

do {
  value++;
  console.log(value);
} while (value < 10);

console.log('Segue o fluxo...');
