/*
    Array
        - Adicionando e removendo um item do array
*/

let users = []

// Adiciona um item no final do Array.
users.push("Pedro")
users.push("João")
users.push("Marcos")

// Adiciona um item no início do Array.
users.unshift("Ana")

// Remove do início do Array
users.shift()

// Remove do final do Array
users.pop()

console.log(users)
